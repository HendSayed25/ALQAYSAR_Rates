class Customer {
  final int id;
  final int uncooperative;
  final int poor;
  final int good;
  final int veryGood;
  final int excellent;
  final String name;
  final String userId;

  Customer({
    required this.id,
    required this.uncooperative,
    required this.poor,
    required this.good,
    required this.veryGood,
    required this.excellent,
    required this.name,
    required this.userId,
  });
}